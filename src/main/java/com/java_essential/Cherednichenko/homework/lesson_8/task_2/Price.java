public class Price {
    private String nameOfGood;
    private String nameOfStorage;
    private double priceOfGood;

    public Price() {
    }

    public String getNameOfGood() {
        return nameOfGood;
    }

    public void setNameOfGood(String nameOfGood) {
        this.nameOfGood = nameOfGood;
    }

    public String getNameOfStorage() {
        return nameOfStorage;
    }

    public void setNameOfStorage(String nameOfStorage) {
        this.nameOfStorage = nameOfStorage;
    }

    public double getPriceOfGood() {
        return priceOfGood;
    }

    public void setPriceOfGood(double priceOfGood) {
        this.priceOfGood = priceOfGood;
    }
}
