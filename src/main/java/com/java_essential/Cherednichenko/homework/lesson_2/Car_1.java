public class Car_1 {
    private int year;
    private String color;

    public Car_1(int year, String color) {
        this.year = year;
        this.color = color;
    }

    public Car_1() {
    }

    public Car_1(int year) {
        this.year = year;
    }

    public int getYear() {
        return year;
    }

    public String getColor() {
        return color;
    }
}
