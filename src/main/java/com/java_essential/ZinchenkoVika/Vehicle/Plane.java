package com.java_essential.ZinchenkoVika.Vehicle;

public class Plane extends Vehicle {
    double height;
    int passengersPlane;

    public double getHeight() {
        return height;
    }
    public void setHeight(double height) {
        this.height = height;
    }
    public int getPassengersPlane() {
        return passengersPlane;
    }
    public void setPassengersPlane(int passengersPlane) {
        this.passengersPlane = passengersPlane;
    }
}
