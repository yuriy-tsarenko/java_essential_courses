package com.java_essential.ZinchenkoVika.Vehicle;

public class Ship extends Vehicle{
    int passengersShip;
    String portOfRegistration;

    public int getPassengersShip() {
        return passengersShip;
    }
    public void setPassengersShip(int passengersShip) {
        this.passengersShip = passengersShip;
    }
    public String getPortOfRegistration() {
        return portOfRegistration;
    }
    public void setPortOfRegistration(String portOfRegistration) {
        this.portOfRegistration = portOfRegistration;
    }
}

