package com.java_essential.kopaiev.essential.homework.lesson2.task2;

public class Car {
    private int year;
    private String color;

    public Car()
    {
    }

    public Car(int year)
    {
        this.year = year;
    }

    public Car(String color)
    {
        this.color = color;
    }

    public Car (int year, String color)
    {
        this.year = year;
        this.color = color;
    }
}
